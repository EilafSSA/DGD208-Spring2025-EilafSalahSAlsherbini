    public string GetAsciiArt(PetType type, string mood)
    {
        if (_asciiArt.TryGetValue(type, out var moodDict))
        {
            if (moodDict.TryGetValue(mood, out var art))
                return art;
            return "(No art for that mood.)";
        }
        return "(No art for that pet type.)";
    }
